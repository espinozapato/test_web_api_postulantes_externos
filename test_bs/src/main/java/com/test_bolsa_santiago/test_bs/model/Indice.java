package com.test_bolsa_santiago.test_bs.model;

public class Indice {
	private String Nombre;
	private Number Valor;
	private Number Mayor;
	private Number Menor;
	private Number Medio;
	private Number Variacion;
	public String getNombre() {
		return Nombre;
	}
	public void setNombre(String nombre) {
		Nombre = nombre;
	}
	public Number getValor() {
		return Valor;
	}
	public void setValor(Number valor) {
		Valor = valor;
	}
	public Number getMayor() {
		return Mayor;
	}
	public void setMayor(Number mayor) {
		Mayor = mayor;
	}
	public Number getMenor() {
		return Menor;
	}
	public void setMenor(Number menor) {
		Menor = menor;
	}
	public Number getMedio() {
		return Medio;
	}
	public void setMedio(Number medio) {
		Medio = medio;
	}
	public Number getVariacion() {
		return Variacion;
	}
	public void setVariacion(Number variacion) {
		Variacion = variacion;
	}
	
	
}
